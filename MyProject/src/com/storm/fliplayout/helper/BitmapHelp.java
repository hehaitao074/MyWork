	/** 缩放Bitmap图片 **/
	@SuppressLint("NewApi")
	public static Bitmap zoomBitmap(Bitmap bitmap, int width, int height) {
		if (isImageBig(bitmap.getWidth() * bitmap.getHeight())) {
			return ThumbnailUtils.extractThumbnail(bitmap, width, height);
		} else {
			return ThumbnailUtils.extractThumbnail(bitmap, width * 2,
					height * 2);
		}
	}

	// 图片压缩大小
	public static final int BIGIMAGESIZE = 1000;

	public static boolean isImageBig(int count) {
		boolean flag = true;
		int i = count / 1024;
		if (i < BIGIMAGESIZE) {
			flag = false;
		}
		return flag;
	}

	public static byte[] Bitmap2Bytes(Bitmap bm) {
		ByteArrayOutputStream baos = new ByteArrayOutputStream();
		bm.compress(Bitmap.CompressFormat.PNG, 100, baos);
		return baos.toByteArray();
	}

	/** 保存到SD卡 **/
	public static void savePhotoToSDCard(String path, String photoName,
			Bitmap photoBitmap) {
		if (android.os.Environment.getExternalStorageState().equals(
				android.os.Environment.MEDIA_MOUNTED)) {
			File dir = new File(path);
			if (!dir.exists()) {
				dir.mkdirs();
			}
			File photoFile = new File(path, photoName); // 在指定路径下创建文件
			FileOutputStream fileOutputStream = null;
			try {
				fileOutputStream = new FileOutputStream(photoFile);
				if (photoBitmap != null) {
					if (photoBitmap.compress(Bitmap.CompressFormat.PNG, 100,
							fileOutputStream)) {
						fileOutputStream.flush();
					}
				}
			} catch (FileNotFoundException e) {
				photoFile.delete();
				e.printStackTrace();
			} catch (IOException e) {
				photoFile.delete();
				e.printStackTrace();
			} finally {
				try {
					fileOutputStream.close();
				} catch (IOException e) {
					e.printStackTrace();
				}
			}
		}
	}

